#include "../libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	write(fd, "-", n < 0);
	if (n == -2147483648)
		write(fd, "2147483648", 10);
	else
	{
		if (n < 0)
			n *= -1;
		if (n >= 10)
			ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd(n % 10 + 48, fd);
	}
}
